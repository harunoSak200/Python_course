#string slicing : 

a = "hello world"
print(a[2:5])

print(a[0:50])  # by putting the ending range  > size of the string will print the string after the given index till the size of the string.


print(a[:7])
print(a[2:])
print(a[2:1])

print(a[-1 :-5])

print(a[-5 :-1])


print(a[-5 :]) #to print the whole string after the -5 index .
print(a[-2 :])
print(a[-2:-2])# it will print the single letter word


